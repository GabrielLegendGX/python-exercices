from math import hypot
print('Calculador de hipotenusa')
oposto=float(input('Qual é o cateto oposto ?'))
deitado=float(input('Qaul é o cateto adjacente ? '))
hipote=hypot(oposto, deitado)
print('A hipotenusa vale {:.2f}'.format(hipote))




